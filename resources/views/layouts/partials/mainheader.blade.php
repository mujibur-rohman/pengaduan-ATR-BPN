<script type="text/javascript">
$(document).ready(function(){
    var badge = $('#badge-notification'),
        list_notif = $('#list-notification');
    
    function row_notif(html, is_read) {
        var cls = is_read === 'N' ? ' list-group-item-secondary' : '';
        return '<li class="list-group-item' + cls + '">' + html + '</li>';
    }
    
    function render_notif(data) {
        var html = '';
        
        if (data.items.length === 0) {
            html = row_notif('Belum ada notification', 'N');
            $(badge).html('');
        } else {
            if (data.total === 0) {
                $(badge).html('');
            } else {
                $(badge).html(data.total > 100 ? "99+" : data.total);
            }
            
            $.each(data.items, function(idx, obj){
                var text = '<a href="' + obj.url + '">' + obj.message + '<a/>';
                
                html+= row_notif(text, obj.is_read);
            });
        }
        $(list_notif).html(html);
    }
    
    function load_notif() {
        $.get("{{ URL::to('/admin/load_notif') }}", {}, function(result){
            if (result.success) {
                render_notif(result.data);
            }
        }, 'json');
    }
    
    load_notif();
    var duration = 5 * 60 * 1000,
        myvar = setInterval(function(){
        load_notif();
    }, duration);
    
    $('#select_role_name').change(function(){
        var data = {
            _token: '{{ csrf_token() }}',
            permission: $(this).val()
        };
        $.post("{{ URL::to('/admin/change_permission') }}", data, function(result){
            console.log(result);
            location.reload();
        }, 'json');
    });
});
</script>